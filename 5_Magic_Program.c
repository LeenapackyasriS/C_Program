#include<stdio.h>   // Standard Input/Output functions (printf, etc.)
#include<conio.h>   // Console I/O functions (getch, etc.)
#include<stdlib.h>  // Standard library functions (system, etc.)

void main() {
    system("cls");       // Clears the console screen
    printf("Leena");     // Prints "Leena" on the screen
    getch();             // Waits for a key press before continuing

    system("cls");       // Clears the screen again
    printf("BEYCAN");    // Prints "BEYCAN" on the screen
    getch();             // Waits for another key press before ending

    system("cls");
    printf("Siva");
    getch();
}
